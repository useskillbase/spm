// --- Deploy tests ---

describe("openclaw deploy", () => {
  it("creates workspace and updates openclaw.json", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    const result = await openclawTarget.deploy(persona, [], {
      agentId: "my-agent",
      configPath,
      overwrite: true,
    });

    expect(result.agentId).toBe("my-agent");
    expect(result.configUpdated).toBe(true);
    expect(result.bindingAdded).toBe(false);

    // Workspace was created with SOUL.md
    const soul = await fs.readFile(path.join(result.workspaceDir, "SOUL.md"), "utf-8");
    expect(soul).toContain("test-agent@1.2.0");

    // openclaw.json was updated
    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    expect(config.agents.list).toEqual(
      expect.arrayContaining([
        expect.objectContaining({ id: "my-agent" }),
      ]),
    );
  });

  it("adds channel binding when --bind-channel specified", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    const result = await openclawTarget.deploy(persona, [], {
      agentId: "tg-agent",
      bindChannel: "telegram",
      configPath,
      overwrite: true,
    });

    expect(result.bindingAdded).toBe(true);

    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    expect(config.bindings).toEqual(
      expect.arrayContaining([
        expect.objectContaining({
          agentId: "tg-agent",
          match: { channel: "telegram" },
        }),
      ]),
    );
  });

  it("adds binding with accountId", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    await openclawTarget.deploy(persona, [], {
      agentId: "wa-agent",
      bindChannel: "whatsapp",
      bindAccountId: "personal",
      configPath,
      overwrite: true,
    });

    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    expect(config.bindings[0].match).toEqual({
      channel: "whatsapp",
      accountId: "personal",
    });
  });

  it("does not duplicate agent on re-deploy", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    await openclawTarget.deploy(persona, [], {
      agentId: "agent1",
      configPath,
      overwrite: true,
    });

    const result = await openclawTarget.deploy(persona, [], {
      agentId: "agent1",
      configPath,
      overwrite: true,
    });

    expect(result.configUpdated).toBe(false);

    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    const agentEntries = config.agents.list.filter(
      (a: { id: string }) => a.id === "agent1",
    );
    expect(agentEntries).toHaveLength(1);
  });

  it("does not duplicate binding on re-deploy", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    await openclawTarget.deploy(persona, [], {
      agentId: "agent1",
      bindChannel: "telegram",
      configPath,
      overwrite: true,
    });

    await openclawTarget.deploy(persona, [], {
      agentId: "agent1",
      bindChannel: "telegram",
      configPath,
      overwrite: true,
    });

    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    const tgBindings = config.bindings.filter(
      (b: { agentId: string; match: { channel: string } }) =>
        b.agentId === "agent1" && b.match.channel === "telegram",
    );
    expect(tgBindings).toHaveLength(1);
  });

  it("preserves existing agents in openclaw.json", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    // Pre-populate with existing agent
    await fs.writeFile(
      configPath,
      JSON.stringify({
        agents: { list: [{ id: "existing", workspace: "/some/path" }] },
        bindings: [{ agentId: "existing", match: { channel: "slack" } }],
      }),
    );

    await openclawTarget.deploy(persona, [], {
      agentId: "new-agent",
      configPath,
      overwrite: true,
    });

    const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
    expect(config.agents.list).toHaveLength(2);
    expect(config.agents.list[0].id).toBe("existing");
    expect(config.agents.list[1].id).toBe("new-agent");
    expect(config.bindings[0].match.channel).toBe("slack");
  });

  it("returns docker fragment", async () => {
    const persona = testPersona();
    const configPath = path.join(tmpDir, "openclaw.json");

    const result = await openclawTarget.deploy(persona, [], {
      agentId: "dock-agent",
      configPath,
      overwrite: true,
    });

    expect(result.dockerFragment).toContain("workspace-dock-agent");
    expect(result.dockerFragment).toContain("/home/node/.openclaw/workspace-dock-agent");
  });
});

// --- Import tests ---

describe("openclaw import", () => {
  it("imports SPM-generated workspace", async () => {
    const persona = testPersona();
    const skills = testSkills();
    const exportDir = path.join(tmpDir, "workspace-test-agent");

    // First export
    await openclawTarget.export(persona, skills, { outputDir: exportDir });

    // Then import
    const imported = await openclawTarget.import!(exportDir);

    expect(imported.name).toBe("test-agent");
    expect(imported.character.role).toBe(persona.character.role);
    expect(imported.character.tone).toBe("direct, concise");
    expect(imported.character.guidelines).toEqual([
      "Always explain tradeoffs",
      "Prefer simple solutions",
    ]);
    expect(imported.character.instructions).toContain("race conditions");
  });

  it("reads skill references from exported skills/ directory", async () => {
    const persona = testPersona();
    const skills = testSkills();
    const exportDir = path.join(tmpDir, "workspace-with-skills");

    await openclawTarget.export(persona, skills, { outputDir: exportDir });

    const imported = await openclawTarget.import!(exportDir);

    expect(imported.skills).toBeDefined();
    expect(imported.skills!["acme/code-review"]).toBe("^2.1.0");
    expect(imported.skills!["acme/testing"]).toBe("^1.0.0");
  });

  it("falls back to AGENTS.md parsing when no skills/ directory", async () => {
    const workspaceDir = path.join(tmpDir, "workspace-agents-only");
    await fs.mkdir(workspaceDir, { recursive: true });
    await fs.writeFile(
      path.join(workspaceDir, "SOUL.md"),
      "## Persona\n\nA helper.\n",
    );
    await fs.writeFile(
      path.join(workspaceDir, "AGENTS.md"),
      "## Skills\n\n- **acme/lint** (v3.0.0) — 500 tokens\n",
    );

    const imported = await openclawTarget.import!(workspaceDir);

    expect(imported.skills).toBeDefined();
    expect(imported.skills!["acme/lint"]).toBe("^3.0.0");
  });

  it("handles workspace without AGENTS.md or skills/", async () => {
    const workspaceDir = path.join(tmpDir, "workspace-minimal");
    await fs.mkdir(workspaceDir, { recursive: true });
    await fs.writeFile(
      path.join(workspaceDir, "SOUL.md"),
      "## Persona\n\nA helpful assistant.\n\n**Tone:** friendly\n",
    );

    const imported = await openclawTarget.import!(workspaceDir);

    expect(imported.character.role).toBe("A helpful assistant.");
    expect(imported.character.tone).toBe("friendly");
    expect(imported.skills).toBeUndefined();
  });

  it("extracts name from directory when no metadata comment", async () => {
    const workspaceDir = path.join(tmpDir, "workspace-my-bot");
    await fs.mkdir(workspaceDir, { recursive: true });
    await fs.writeFile(
      path.join(workspaceDir, "SOUL.md"),
      "## Persona\n\nA bot.\n",
    );

    const imported = await openclawTarget.import!(workspaceDir);
    expect(imported.name).toBe("my-bot");
  });

  it("throws when SOUL.md is missing", async () => {
    const workspaceDir = path.join(tmpDir, "workspace-empty");
    await fs.mkdir(workspaceDir, { recursive: true });

    await expect(openclawTarget.import!(workspaceDir)).rejects.toThrow(
      "SOUL.md not found",
    );
  });

  it("skips style note during import (derived from settings)", async () => {
    const workspaceDir = path.join(tmpDir, "workspace-style-note");
    await fs.mkdir(workspaceDir, { recursive: true });
    await fs.writeFile(
      path.join(workspaceDir, "SOUL.md"),
      [
        "## Persona",
        "",
        "A precise analyst.",
        "",
        "**Tone:** formal",
        "",
        "**Style note:** Be precise, factual, and conservative in your responses.",
        "",
      ].join("\n"),
    );

    const imported = await openclawTarget.import!(workspaceDir);
    expect(imported.character.role).toBe("A precise analyst.");
    expect(imported.character.tone).toBe("formal");
    // Style note should not appear in instructions
    expect(imported.character.instructions).toBeUndefined();
  });
});

// --- Export → Import roundtrip ---

describe("export → import roundtrip", () => {
  it("preserves character fields through roundtrip", async () => {
    const original = testPersona();
    const exportDir = path.join(tmpDir, "roundtrip");

    await openclawTarget.export(original, [], { outputDir: exportDir });
    const imported = await openclawTarget.import!(exportDir);

    expect(imported.character.role).toBe(original.character.role);
    expect(imported.character.tone).toBe(original.character.tone);
    expect(imported.character.guidelines).toEqual(original.character.guidelines);
    expect(imported.character.instructions).toBe(original.character.instructions);
  });

  it("preserves character with minimal fields", async () => {
    const original = testPersona({
      character: {
        role: "A simple helper.",
      },
    });
    const exportDir = path.join(tmpDir, "roundtrip-minimal");

    await openclawTarget.export(original, [], { outputDir: exportDir });
    const imported = await openclawTarget.import!(exportDir);

    expect(imported.character.role).toBe("A simple helper.");
    expect(imported.character.tone).toBeUndefined();
    expect(imported.character.guidelines).toBeUndefined();
  });

  it("preserves persona name through roundtrip", async () => {
    const original = testPersona({ name: "my-custom-agent" });
    const exportDir = path.join(tmpDir, "roundtrip-name");

    await openclawTarget.export(original, [], { outputDir: exportDir });
    const imported = await openclawTarget.import!(exportDir);

    expect(imported.name).toBe("my-custom-agent");
  });
});

