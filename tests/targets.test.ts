import { describe, it, expect, beforeEach, afterEach } from "vitest";
import fs from "node:fs/promises";
import path from "node:path";
import os from "node:os";
import { openclawTarget } from "../src/targets/openclaw.js";
import type { PersonaManifest, LoadedSkill } from "../src/types/index.js";

let tmpDir: string;

beforeEach(async () => {
  tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), "spm-openclaw-test-"));
});

afterEach(async () => {
  await fs.rm(tmpDir, { recursive: true, force: true });
});

function testPersona(overrides: Partial<PersonaManifest> = {}): PersonaManifest {
  return {
    schema_version: 1,
    name: "test-agent",
    version: "1.2.0",
    description: "A test persona",
    author: "tester",
    license: "MIT",
    character: {
      role: "Senior backend engineer specializing in distributed systems.",
      tone: "direct, concise",
      guidelines: [
        "Always explain tradeoffs",
        "Prefer simple solutions",
      ],
      instructions: "When reviewing code, check for race conditions first.",
    },
    ...overrides,
  };
}

function testSkills(): LoadedSkill[] {
  return [
    {
      name: "acme/code-review",
      version: "2.1.0",
      content: "# Code Review Skill\n\nReview code for correctness and security.\n",
      permissions: ["file:read"],
      tokens_estimate: 1500,
    },
    {
      name: "acme/testing",
      version: "1.0.0",
      content: "# Testing Skill\n\nWrite and run tests.\n",
      permissions: [],
      tokens_estimate: 800,
    },
  ];
}

// --- Export tests ---

describe("openclaw export", () => {
  it("creates SOUL.md with persona character instructions", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-basic");

    const result = await openclawTarget.export(persona, [], { outputDir });

    expect(result.files).toContain("SOUL.md");
    const soul = await fs.readFile(path.join(outputDir, "SOUL.md"), "utf-8");
    expect(soul).toContain("<!-- Generated by SPM from persona: test-agent@1.2.0 -->");
    expect(soul).toContain("## Persona");
    expect(soul).toContain("Senior backend engineer");
    expect(soul).toContain("**Tone:** direct, concise");
    expect(soul).toContain("- Always explain tradeoffs");
    expect(soul).toContain("- Prefer simple solutions");
    expect(soul).toContain("When reviewing code, check for race conditions first.");
  });

  it("creates AGENTS.md with skill listing", async () => {
    const persona = testPersona();
    const skills = testSkills();
    const outputDir = path.join(tmpDir, "export-skills");

    const result = await openclawTarget.export(persona, skills, { outputDir });

    expect(result.files).toContain("AGENTS.md");
    const agents = await fs.readFile(path.join(outputDir, "AGENTS.md"), "utf-8");
    expect(agents).toContain("<!-- Generated by SPM from persona: test-agent@1.2.0 -->");
    expect(agents).toContain("## Skills");
    expect(agents).toContain("- **acme/code-review** (v2.1.0) — 1500 tokens");
    expect(agents).toContain("- **acme/testing** (v1.0.0) — 800 tokens");
    expect(agents).toContain("skills/acme/code-review/SKILL.md");
    expect(agents).toContain('skill_load("skill-name")');
    expect(agents).toContain('skill_search("query")');
  });

  it("exports skill files into skills/ directory", async () => {
    const persona = testPersona();
    const skills = testSkills();
    const outputDir = path.join(tmpDir, "export-skill-files");

    const result = await openclawTarget.export(persona, skills, { outputDir });

    // SKILL.md files exported
    expect(result.files).toContain("skills/acme/code-review/SKILL.md");
    expect(result.files).toContain("skills/acme/testing/SKILL.md");
    expect(result.files).toContain("skills/acme/code-review/skill.json");
    expect(result.files).toContain("skills/acme/testing/skill.json");

    // Verify content
    const reviewContent = await fs.readFile(
      path.join(outputDir, "skills", "acme/code-review", "SKILL.md"),
      "utf-8",
    );
    expect(reviewContent).toContain("Review code for correctness and security");

    const testContent = await fs.readFile(
      path.join(outputDir, "skills", "acme/testing", "SKILL.md"),
      "utf-8",
    );
    expect(testContent).toContain("Write and run tests");

    // Verify metadata
    const meta = JSON.parse(
      await fs.readFile(
        path.join(outputDir, "skills", "acme/code-review", "skill.json"),
        "utf-8",
      ),
    );
    expect(meta.name).toBe("acme/code-review");
    expect(meta.version).toBe("2.1.0");
    expect(meta.tokens_estimate).toBe(1500);
    expect(meta.permissions).toEqual(["file:read"]);
  });

  it("creates AGENTS.md without skills section when no skills", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-no-skills");

    await openclawTarget.export(persona, [], { outputDir });

    const agents = await fs.readFile(path.join(outputDir, "AGENTS.md"), "utf-8");
    expect(agents).not.toContain("### Exported skills:");
    expect(agents).toContain("No skills exported");
  });

  it("creates config/mcporter.json", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-mcporter");

    const result = await openclawTarget.export(persona, [], { outputDir });

    expect(result.files).toContain("config/mcporter.json");
    const raw = await fs.readFile(path.join(outputDir, "config", "mcporter.json"), "utf-8");
    const config = JSON.parse(raw);
    expect(config.mcpServers.spm).toBeDefined();
    expect(config.mcpServers.spm.command).toBeDefined();
    expect(config.mcpServers.spm.args).toContain("serve");
  });

  it("creates skills/ directory", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-dirs");

    await openclawTarget.export(persona, [], { outputDir });

    const stat = await fs.stat(path.join(outputDir, "skills"));
    expect(stat.isDirectory()).toBe(true);
  });

  it("does not overwrite existing mcporter.json", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-preserve");
    const mcporterDir = path.join(outputDir, "config");
    await fs.mkdir(mcporterDir, { recursive: true });
    await fs.writeFile(
      path.join(mcporterDir, "mcporter.json"),
      '{"mcpServers":{"custom":{"command":"custom"}}}',
    );

    const result = await openclawTarget.export(persona, [], { outputDir, overwrite: true });

    expect(result.files).not.toContain("config/mcporter.json");
    const raw = await fs.readFile(path.join(mcporterDir, "mcporter.json"), "utf-8");
    expect(raw).toContain('"custom"');
  });

  it("throws when output directory exists without --overwrite", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-exists");
    await fs.mkdir(outputDir, { recursive: true });

    await expect(
      openclawTarget.export(persona, [], { outputDir }),
    ).rejects.toThrow("Output directory already exists");
  });

  it("succeeds with overwrite flag on existing directory", async () => {
    const persona = testPersona();
    const outputDir = path.join(tmpDir, "export-overwrite");
    await fs.mkdir(outputDir, { recursive: true });

    const result = await openclawTarget.export(persona, [], { outputDir, overwrite: true });
    expect(result.files).toContain("SOUL.md");
  });
});

